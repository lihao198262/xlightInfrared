#ifndef __TIMER_H
#define __TIMER_H


/*******************************************************************************
 * 名称: sleep_ms
 * 功能: 休眠 毫秒
 * 形参: uint32_t ms 休眠的毫秒数
 * 返回: 无
 * 说明: 无 
 ******************************************************************************/
void sleep_ms(uint32_t ms);

/*******************************************************************************
 * 名称: sleep_us
 * 功能: 休眠微秒 范围(0-1000]毫秒
 * 形参: uint32_t us
 * 返回: 无
 * 说明: 无 
 ******************************************************************************/
void sleep_us(uint32_t us);
#endif
